CREATE TABLE eve_universe (
    PRIMARY KEY (id),
    id    integer,
    name  varchar(50) NOT NULL
);

CREATE TABLE esi_responses (
    PRIMARY KEY (id),
    id              serial,
    code            integer             DEFAULT 200,
    last_modified   timestamp NOT NULL  DEFAULT now(),
    expiry          timestamp
);

CREATE TABLE fw_system_statuses (
    PRIMARY KEY (id),
    id                        serial,
    response_id               integer NOT NULL REFERENCES esi_responses(id),
    solar_system_id           integer NOT NULL REFERENCES eve_universe(id),
    victory_points            integer NOT NULL,
    victory_points_threshold  integer NOT NULL DEFAULT 3000,
    occupier_faction_id       integer NOT NULL REFERENCES eve_universe(id)
);

